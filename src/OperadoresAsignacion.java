public class OperadoresAsignacion {
    public static void main(String[] args) {
        // Operador =
        int x = 13, y = 7, z;
        z = 5;
        System.out.println("Output 1 de x = " + x);

        // Operador +=
        x += y; // x = x + y
        System.out.println("Output 2 de x = " + x);

        // Operador -=
        x -= y; // x = x - y
        System.out.println("Output 3 de x = " + x);

        // operador *=
        x *= y; // x = x * y
        System.out.println("Output 4 de x = " + x);

        // Operador /=
        x /= y; // x = x / y
        System.out.println("Output 5 de x = " + x);

        // Operador %=
        x %= y; // x = x % y
        System.out.println("Output 6 de x = " + x);
    }
}
